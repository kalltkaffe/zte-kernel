static int atmel_platform_power(int on)
{
	int rc = -EINVAL;
 
 #if defined (CONFIG_MACH_TURIES)
 	rc = gpio_request(89, "touch voltage");
	if (rc) {
		pr_err("%s: unable to request gpio 89 (%d)\n",
			__func__, rc);
	}
	if (on){
        gpio_direction_output(89, 1);
	}
else
  gpio_direction_output(89, 0);
  gpio_free(89);
  
  
 #elif defined (CONFIG_MACH_MOONCAKE)
  rc = gpio_request(30, "touch voltage");
	if (rc) {
		pr_err("%s: unable to request gpio 89 (%d)\n",
			__func__, rc);
	}
	if (on){
        gpio_direction_output(30, 1);
	}
else
  gpio_direction_output(30, 0);
  gpio_free(30);
 #else
 
	rc = gpio_request(109, "touch voltage");
	if (rc) {
		pr_err("%s: unable to request gpio 109 (%d)\n",
			__func__, rc);
	}
	if (on){
        gpio_direction_output(109, 1);
	}
else
  gpio_direction_output(109, 0);
  gpio_free(109);
 #endif
	return 0;
}


